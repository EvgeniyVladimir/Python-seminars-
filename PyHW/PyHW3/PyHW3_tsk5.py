def fibonacci(number):
    if number == 1 or number == 2:
        return 1
    else:
        return fibonacci(number - 1) + fibonacci(number - 2) 
num = int(input("Введите число n: "))
result = list()
for i in range(1, num + 1):
    result.append(fibonacci(i)*((-1)**(i+1)))
result.reverse()
result.append(0)
for i in range(1, num + 1):
    result.append(fibonacci(i))
print(*result)


